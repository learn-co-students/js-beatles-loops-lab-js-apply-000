function theBeatlesPlay(musicians, instruments) {
  const arr = []
  
  for (let i = 0; i < musicians.length; i++) {
    arr.push(`${musicians[i]} plays ${instruments[i]}`)
  }
  return arr
  
}

function johnLennonFacts(facts){
  const hisFacts = []
  let n = 0
  while (n < facts.length){
    hisFacts.push(`${facts[n]}!!!`)
    n++;
  }
  return hisFacts
  
}

